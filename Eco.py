@bot.command('Батарейка')
async def battery(ctx):
    await ctx.send("Утилизация батареек должна происходить согласно санитарным нормам, то есть в специализированный контейнер. На каждом таком изделии есть обозначение, что его нельзя выкидывать вместе с обычными бытовыми отходами.")

